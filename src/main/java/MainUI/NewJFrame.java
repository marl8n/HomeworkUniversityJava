    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
        this.usuarios.add(new User(
                this.textFieldNombre.getText(),
                this.textFieldDPI.getText(),
                this.textFIeldSaldo.getText()
        ));
        this.textFieldNombre.setText("");
        this.textFieldDPI.setText("");
        this.textFIeldSaldo.setText("");
    }//GEN-LAST:event_addButtonMouseClicked

    private void buttonSarchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonSarchMouseClicked
        for ( int i = 0; i < this.usuarios.size(); i++ ){
            if (usuarios.get(i).DPI.toString().equals(this.textFieldBusqueda.getText())){
                this.labelName.setText("Nombre: " + this.usuarios.get(i).nombre);
                this.labelDPI.setText("DPI: " + this.usuarios.get(i).DPI);
                this.labelSaldo.setText("Saldo: " + this.usuarios.get(i).saldo.toString());
                idx = i;
            }
        }
    }//GEN-LAST:event_buttonSarchMouseClicked

    private void buttonDoOperationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonDoOperationMouseClicked
        if(this.radioButtonDeposito.isSelected()){
            usuarios.get(idx).saldo += Double.parseDouble(textFieldMoney.getText());
            textFieldMoney.setText("");
            labelName.setText("Nombre: ");
            labelDPI.setText("DPI: ");
            labelSaldo.setText("Saldo: ");
            idx = -1;
            textFieldMoney.setText("");
            labelName.setText("Nombre: ");
            labelDPI.setText("DPI: ");
            labelSaldo.setText("Saldo: ");
            textFieldBusqueda.setText("");
        }
        else if(this.radioButtonRetiro.isSelected()){
            if(usuarios.get(idx).saldo < Double.parseDouble(textFieldMoney.getText() ) ){
                JOptionPane.showMessageDialog(rootPane, "No tiene fondos suficientes!");
            }
            else{
                usuarios.get(idx).saldo -= Double.parseDouble(textFieldMoney.getText());
                textFieldMoney.setText("");
                labelName.setText("Nombre: ");
                labelDPI.setText("DPI: ");
                labelSaldo.setText("Saldo: ");
                idx = -1;
                textFieldBusqueda.setText("");
            }
        }
    }//GEN-LAST:event_buttonDoOperationMouseClicked

